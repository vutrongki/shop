<script>
    function addToCart() {
        let quantity = $('.num-product').val();
        let productColor = $('.product-color').data('color');
        let productSize = $('.product-size').data('size');
        let url = $(this).data('url') + '?color=' + productColor + '&size=' + productSize + '&quantity=' + quantity;
        $.ajax({
            type: 'GET',
            url: url,
            dataType: 'json',
            success: function(data) {
                if (data.code === 200) {
                    swal("Add product success!");
                        setTimeout(function() {
                            location.reload();
                        }, 2000)
                }
            },
            error: function() {

            }
        })
    }

    $(function() {
        $('#add').on('click', addToCart);
    })
</script>
